package oops;

public class Car {

    private int speed;
    private int numberOfTyers;

    public void setSpeed(int s){
        speed = s;
    }

    public int getSpeed(){
        return speed;
    }

    public void setNumberOfTyers(int numberOfTyers) {
        this.numberOfTyers = numberOfTyers;
    }

    public int getNumberOfTyers() {
        return numberOfTyers;
    }

}
